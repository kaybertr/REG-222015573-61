public class WhileLoop {

  public static void main(String[] args) throws Exception {

    int i = 1;
    
    // Loop while i is less than or equal to 5
    while(i <= 5) {
    
      // Print the value of i  
      System.out.println(i); 
      
      // Increment i after each iteration
      i++; 
    }
  }
}
